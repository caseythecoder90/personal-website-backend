package com.caseyquinn.personal_website.entity.enums;

/**
 * Enumeration of blog post image types.
 * Used to categorize images for display and organization purposes.
 */
public enum BlogImageType {

    /**
     * Main header/preview image for the blog post.
     */
    FEATURED,

    /**
     * Image embedded within the blog content.
     */
    INLINE,

    /**
     * Part of an image gallery within the post.
     */
    GALLERY,

    /**
     * Small preview version of an image.
     */
    THUMBNAIL
}
